package com.dalaiye.luhang.bean;

import java.util.List;

/**
 * @author admin
 * @date 2019/4/12
 * @function 注释
 **/
public class MessageBean {

    /**
     * total : string,总行数
     * pageNumber : string,分页
     * rows : [{"messageId":"string,消息id","title":"string,消息名称","content":"string,消息内容","messageType":"string,消息类型","messageTime":"string,时间","isRead":"string,是否已读0否1是"}]
     */

    private String total;
    private String pageNumber;
    private List<RowsBean> rows;

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getPageNumber() {
        return pageNumber;
    }

    public void setPageNumber(String pageNumber) {
        this.pageNumber = pageNumber;
    }

    public List<RowsBean> getRows() {
        return rows;
    }

    public void setRows(List<RowsBean> rows) {
        this.rows = rows;
    }

    public static class RowsBean {
        /**
         * messageId : string,消息id
         * title : string,消息名称
         * content : string,消息内容
         * messageType : string,消息类型
         * createDate : string,时间
         * isRead : string,是否已读0否1是
         */

        private String messageId;
        private String title;
        private String content;
        private String messageType;
        private String createDate;
        private String isRead;

        public String getMessageId() {
            return messageId;
        }

        public void setMessageId(String messageId) {
            this.messageId = messageId;
        }

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }

        public String getContent() {
            return content;
        }

        public void setContent(String content) {
            this.content = content;
        }

        public String getMessageType() {
            return messageType;
        }

        public void setMessageType(String messageType) {
            this.messageType = messageType;
        }

        public String getCreateDate() {
            return createDate;
        }

        public void setCreateDate(String createDate) {
            this.createDate = createDate;
        }

        public String getIsRead() {
            return isRead;
        }

        public void setIsRead(String isRead) {
            this.isRead = isRead;
        }
    }
}
