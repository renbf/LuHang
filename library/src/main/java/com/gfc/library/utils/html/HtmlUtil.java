package com.gfc.library.utils.html;

/**
 * @author Administrator
 * @date 2018/11/29
 * @function 功能
 */
public class HtmlUtil {

    public static String getWebLoadData(String title, String time, String content) {
        String head = "<head>" +
                "<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0, user-scalable=no\"> " +
                "<style>img{max-width: 100%; display:block;max-height:100%; } </style>" +
                "</head>";
        return "<html style=''>"
                + head + "<body style=''><h4 style='font-size:14px;margin:0;padding:0;'>"
                + title + "</h4><p style='font-size:12px;color:#979797; margin:0; margin-top:3px;padding:0;'>"
                + time + "</p>"
                + content + "</body></html>";
    }

    public static String getWebLoadData(String content) {
        String head = "<head>" +
                "<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0, user-scalable=no\"> " +
                "<style>img{max-width: 100%; display:block;max-height:100%; } </style>" +
                "</head>";
        return "<html>" + head + "<body>" + content + "</body></html>";
    }
}
